package zohirul_Alom;

public class String_7 {
	//Primitive to String
	// String to Primitive

	public static void main(String[] args) {
		
		//Primitive to String 
		int x = 40;
		String y = Integer.toString(x);
		System.out.println("Y = "+y);
		
		//String to Primitive 
		String m ="30";
		double n = Double.parseDouble(m);
		System.out.println("N = "+n);
		
		

	}

}
