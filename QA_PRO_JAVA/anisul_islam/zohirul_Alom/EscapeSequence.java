public class EscapeSequence {

	/*
	 * Escape Sequence
	 *  \b    backspace
     *  \t    tab
     *  \n    new line
     *  \r    carriage return
     *  \”    double quote
     *  \’    single quote
     *  \\    backslash
     *  
     *  I will show you how to use Escape sequence below.....

	 * */
	
	public static void main(String[] args) {
		
		// print out information using Escape Sequence....
		System.out.println(" ZOHIRUL ALOM \n 106-09 79TH ST \n OZONE PARK NY, 11417 \n UNITED STATE OF AMERICA \n ");
		
		// Print out message with double quote .....
		System.out.println(" WHAT IS AN ESCAPE SEQUENCE? \n \"IT IS A SPECIAL CHARACTER FOLLOWED BY BACKSLASH\" \n ");
		// print out message with backslash .......
		System.out.println(" \"ESCAPE SEQUENCE\" ");


	}

}
